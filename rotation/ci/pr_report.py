def main():
    """CI → PR Report 主流程 (由 GitHub Actions 调用)"""
    import sys
    
    print("🧠 CI PR Report Generator")
    print("=" * 50)
    
    # 检查是否在 PR 环境
    from .github_comment import get_pr_number
    pr_number = get_pr_number()
    
    if not pr_number:
        print("⚠️ 非 PR 环境，生成本地报告")
        # 本地模式: 只生成不发布
        return
    
    print(f"📝 PR #{pr_number} — 生成审计报告...")
    
    # 从最近的 CI 结果加载
    import os, json
    report_dir = os.path.join(os.path.dirname(__file__), "reports")
    reports = sorted([f for f in os.listdir(report_dir) if f.endswith('.json')], reverse=True) if os.path.exists(report_dir) else []
    
    if not reports:
        print("⚠️ 无 CI 报告可用")
        return
    
    # 加载最新报告
    with open(os.path.join(report_dir, reports[0])) as f:
        ci_result = json.load(f)
    
    # 生成研究员报告
    report = build_researcher_report(ci_result)
    
    # 发布到 PR
    result = update_or_create_comment(report, pr_number)
    
    if result.get("posted"):
        print(f"✅ 审计报告已发布到 PR #{pr_number}")
    else:
        print(f"❌ 发布失败: {result.get('error', 'unknown')}")


if __name__ == "__main__":
    main()
